package com.chinamcloud.api.controller;

import com.chinamcloud.api.CodeResult;
import com.chinamcloud.spider.dao.SearchKeywordDao;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;

import java.util.Map;

@RestController
@CrossOrigin
public class SearchKwController {

    private SearchKeywordDao searchKeywordDao;

    {
        searchKeywordDao = new SearchKeywordDao();
    }

    @PostMapping("/searchKw")
    public CodeResult add(@RequestBody Map<String, Object> map) {
        return searchKeywordDao.add(map) ? CodeResult.successResult() : CodeResult.failedResult("failed");
    }
}









































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































